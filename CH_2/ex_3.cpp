#include<iostream>
int main()
{
    // taking 'a' as a integer constant and using assignment operator
    int a = 10;  
    std::cout << a << std::endl;

    // updating value of a using assignment operator
    a = 20;
    std::cout << a << std::endl;

    // using decrement operator
    --a;
    std::cout << a << std::endl;

    return 0;
}